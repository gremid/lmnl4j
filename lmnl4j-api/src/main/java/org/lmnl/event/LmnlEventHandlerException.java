package org.lmnl.event;

public class LmnlEventHandlerException extends Exception {

	private static final long serialVersionUID = 1L;

	public LmnlEventHandlerException() {
		super();
	}

	public LmnlEventHandlerException(String message, Throwable cause) {
		super(message, cause);
	}

	public LmnlEventHandlerException(String message) {
		super(message);
	}

	public LmnlEventHandlerException(Throwable cause) {
		super(cause);
	}
}
